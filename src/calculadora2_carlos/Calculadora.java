    private void jButton27ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton27ActionPerformed
this.numdos=Float.parseFloat(this.resultado.getText());

switch(this.operador){
    case
            "Potencia" -> this.resultado.setText(Double.toString(Math.pow(numuno, numdos)));
    case
            "Promedio" -> this.resultado.setText(Float.toString((numuno+numdos)/2));
    case
            "Porcentaje" -> this.resultado.setText(Float.toString((numuno*numdos)/100));
    case
            "Raiz" -> this.resultado.setText(Double.toString(Math.sqrt(numuno)));
}
    }//GEN-LAST:event_jButton27ActionPerformed

